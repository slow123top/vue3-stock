          <section>
            <h2>1、什么是二次筛选指标</h2>
            <p>当模型按照您设定的参数在某个交易日选出股票后，二次筛选指标帮助模型判断这些股票买入的优先级。</p>
            <p>例如：最大每日持股数为5，模型在第一个交易日选出了10个股票，此时模型需要按照您设定的二次筛选指标来对这10个股票排序，对前5个股票执行买入操作。</p>
          </section>
          <section>
            <h2>2、什么是风控指标</h2>
            <p>风控是风险控制的缩写，当某个交易日不满足您设定的风控指标，则模型会执行清仓操作（无论股票是否在持有期内），尝试卖出持有的所有股票（停牌、跌停开盘的股票无法卖出）。</p>
            <p>
              风控指标帮助您更好的控制系统性风险，提高模型的稳定性并缩小模型的最大回撤。
            </p>
          </section>
          <section>
            <h2>3、“并且、或者、依次剔除掉”是什么</h2>
            <p>在千投量化内部，将该功能简称为：”与或非”功能。您可以通过”与或非”功能来选择指标之间的组合逻辑。</p>
            <p>例如：A指标为”总市值介于10亿至20亿”，B指标为”总市值介于15亿至25亿之间”。当您构建的模型同时设定了A和B</p>
            <ul>
              <li>选择”并且”，表示您希望模型选出总市值介于15亿至20亿之间的股票。</li>
              <li>选择”或者”，表示您希望模型选出总市值介于10亿至25亿之间的股票。</li>
              <li>选择”依次剔除掉”，当A指标在B指标的上方，表示您希望选出总市值介于10亿至15亿之间的股票。</li>
              <li>选择”依次剔除掉”，当B指标在A指标的上方，表示您希望选出总市值介于20亿至25亿之间的股票。</li>
            </ul>
            <p>千投量化平台允许您将已保存的模型作为指标添加进新的模型中，且不会影响已保存模型内部的”与或非”设定。您可以通过这个技巧组合出极复杂的选股逻辑。</p>
          </section>
