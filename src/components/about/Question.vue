        <section>
          <h1>第五类:选股指标类</h1>
          <section>
            <h2>1、倒数第N个交易日怎么理解</h2>
            <p>模型选中股票的交易日即为倒数第一个交易日。</p>
            <p>
              举个例子：模型在2016­01­08买入股票A，那么2016­01­07即为模型选中股票A的日期（收盘后计算，下一个交易日开盘执行），同时也是倒数第一个交易日。倒数第二个交易日为2016­01­06，倒数第三个交易日为2016­01­05，以此类推。</p>
          </section>
          <section>
            <h2>2、按时间顺序怎么理解</h2>
            <p>指标中常常出现的”倒数”，指的是由现在朝过去方向数。而”按照时间顺序”刚好与之相反，它指的是由过去朝现在数，也即”正数”。</p>
            <p>
              举个例子：模型在2016­01­08买入股票A，假设仅有一个指标起作用：”倒数第N1~第N2个交易日的收盘价按时间顺序依次下降“，参数为1、3，倒数第1~第3个交易日分别为：2016­01­07、2016­01­06、2016­01­05。则模型中的含义为：股票A于2016­01­05的收盘价大于等于股票A于2016­01­06的收盘价，并且股票A于2016­01­06的收盘价大于等于股票A于2016­01­07的收盘价。</p>
          </section>
          <section>
            <h2>3、为什么经典指标选出的股票与我检验的不一致</h2>
            <p>
              部分经典类指标与计算的时间区间强相关，例如：MACD、KDJ等，千投量化平台会严格按照您设定的回测起点、回测终点截取数据并重新计算经典指标，由于经典指标本身计算公式的限定，故而无法与第三方交易软件显示的指标保持完全一致（计算的起点不一样）。</p>
            <p class="explain">
              这并不会影响您的使用，经典指标的内部逻辑、涨跌判断逻辑并没有任何人为修改。
            </p>
