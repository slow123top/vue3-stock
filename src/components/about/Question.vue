          <section>
            <h2>1、什么是持有期</h2>
            <p>
              当模型买入一支股票，默认持有的交易日数量。无论该股票走势如何，在持有期内不会卖出。持有期结束，模型会判断该股票是否仍然符合设定的参数，若不符合，则执行卖出操作。持有期从买入时开始计数，即：买入的交易日为持有期的第一天。</p>
            <p class="explain">
              若您设定了风控指标，并且风控指标提示卖出，无论持有的股票是否仍然在持有期内，均会执行卖出操作。
            </p>
          </section>
