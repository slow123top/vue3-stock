            <h2>2、什么是回测起点、回测终点</h2>
            <p>本次回测的起始日期和本次回测的终止日期，千投量化平台目前支持从2006年1月4日至当前最新已收盘交易日范围内的回测计算。</p>
            <p class="explain">
              您可以任意选择一个时间范围进行回测，检验您的模型在各个行情趋势下的表现，帮助您客观、综合地评价模型。
            </p>
          </section>
          <section>
            <h2>3、什么是买入费率、卖出费率</h2>
            <p>本次回测中，执行买入、卖出操作时所需要付出的交易费用，单位为”万分之”。默认买入费率为万分之三，卖出费率为千分之十三（万分之三的手续费加千分之一的印花税）。</p>
          </section>
          <section>
            <h2>4、什么是模型压力费率</h2>
            <p>
              在实际交易中，往往无法刚好买入或者卖出于指定的价格上，会根据实际情况产生一定的价格差距，该价格差距使得您无法紧跟模型的收益。模型压力费率正是为解决该问题而设定，其每增加一，表示同时增加万分之一的买入费率和万分之一的卖出费率。</p>
            <p class="explain">
              千投量化平台建议您增加模型压力费率，直至每一次调仓换股时，您的实际当日收益略大于模型计算的值。这样可以最大程度地帮您抹平由于操作而造成的收益差距，提高回测报告的可靠性。
            </p>
          </section>
          <section>
            <h2>5、什么是最大每日持股数</h2>
            <p>本次回测中，各个交易日允许最大持有的股票数量。</p>
            <p>例如：最大每日持股数为5，当模型按照您设定的指标，在第一个交易日选出了10个股票并按照二次筛选指标排序后，只会对前5个股票执行买入操作。</p>
          </section>
